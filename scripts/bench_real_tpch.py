#!/usr/bin/env python3
"""
Reproducible TPC-H benchmark using official TPC-H data.

Runs all 22 TPC-H queries against real TPC-H data generated by DuckDB's
built-in dbgen (a faithful port of the official TPC-H data generator).

Step 1 — Generate the data (once):
    pixi run python3 scripts/gen_tpch_parquet.py --sf 1

Step 2 — Run this benchmark:
    pixi run python3 scripts/bench_real_tpch.py --data-dir data/tpch_sf1

Step 3 — Compare with synthetic baseline:
    pixi run python3 scripts/bench_simple.py --rows 1000000 --runs 3

Output columns:
    Pandas   — pandas 2.x (CPU, single-threaded)
    Polars   — Polars lazy API (CPU, multithreaded)
    MX CPU   — MXFrame Mojo AOT kernels (CPU)
    MX GPU   — MXFrame Mojo AOT kernels (GPU via Modular MAX Engine)

Data provenance:
    DuckDB dbgen generates data according to the TPC-H specification v3.0.1.
    Only the benchmark design (which engines to compare, harness code) is
    original work; queries and data follow the TPC-H standard.

    ⚠️  TPC-H® is a trademark of the Transaction Processing Performance
    Council. This is an independent, non-official benchmark.  Results are
    NOT a TPC-certified benchmark and may not be reported as such.
    Reference: https://www.tpc.org/tpch/

Usage:
    pixi run python3 scripts/bench_real_tpch.py \\
        --data-dir data/tpch_sf1   \\
        --runs 3                   \\
        --queries 1,3,6,12         \\
        [--skip-gpu]               \\
        [--skip-polars]
"""
import argparse
import os
import sys
import time

import pyarrow as pa
import pyarrow.parquet as pq
from max import driver as _driver

sys.path.insert(0, os.path.dirname(__file__))
import benchmark_tpch as bm

try:
    import polars as pl
    _POLARS = True
except ImportError:
    _POLARS = False


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

def _safe_gpu_count() -> int:
    try:
        return int(_driver.accelerator_count())
    except Exception:
        return 0


def _time(fn, runs: int) -> float:
    fn()  # warmup
    best = float("inf")
    for _ in range(runs):
        t0 = time.perf_counter()
        fn()
        best = min(best, (time.perf_counter() - t0) * 1000.0)
    return best


def _fmt(v):
    return f"{v:>8.1f}ms" if v is not None else f"{'—':>9}"


def _print_header():
    print(f"\n{'Query':<30} {'Pandas':>9} {'Polars':>9} {'MX CPU':>9} {'MX GPU':>9}")
    print(f"{'—'*30} {'—'*9} {'—'*9} {'—'*9} {'—'*9}")


def _print_row(label, pd_ms, pl_ms, cpu_ms, gpu_ms):
    print(f"  {label:<28} {_fmt(pd_ms)} {_fmt(pl_ms)} {_fmt(cpu_ms)} {_fmt(gpu_ms)}")


# ---------------------------------------------------------------------------
# Data loading
# ---------------------------------------------------------------------------

def load_tables(data_dir: str) -> dict[str, pa.Table]:
    """Load all 8 TPC-H Parquet files into a dict of PyArrow tables."""
    names = ["lineitem", "orders", "customer", "part",
             "supplier", "partsupp", "nation", "region"]
    tables: dict[str, pa.Table] = {}
    print(f"Loading TPC-H data from: {data_dir}")
    for name in names:
        path = os.path.join(data_dir, f"{name}.parquet")
        if not os.path.exists(path):
            print(f"  WARNING: {path} not found — skipping")
            continue
        t = pq.read_table(path)
        tables[name] = t
        print(f"  {name:<12} {len(t):>10,} rows")
    print()
    return tables


# ---------------------------------------------------------------------------
# Polars helpers
# ---------------------------------------------------------------------------

def _to_polars(t: pa.Table):
    """Convert PyArrow table to Polars DataFrame."""
    if not _POLARS:
        return None
    return pl.from_arrow(t)


# ---------------------------------------------------------------------------
# Main
# ---------------------------------------------------------------------------

def main():
    parser = argparse.ArgumentParser(
        description="TPC-H benchmark with official data",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog=__doc__,
    )
    parser.add_argument("--data-dir", default="data/tpch_sf1",
                        help="Directory containing TPC-H Parquet files (default: data/tpch_sf1)")
    parser.add_argument("--runs", type=int, default=3,
                        help="Number of timed hot runs (default: 3)")
    parser.add_argument("--queries", type=str, default=None,
                        help="Comma-separated query numbers, e.g. 1,3,6,12")
    parser.add_argument("--skip-gpu", action="store_true",
                        help="Skip GPU runs even if a GPU is present")
    parser.add_argument("--skip-polars", action="store_true",
                        help="Skip Polars runs")
    args = parser.parse_args()

    # Resolve data-dir relative to repo root if not absolute
    data_dir = args.data_dir
    if not os.path.isabs(data_dir):
        repo_root = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
        data_dir = os.path.join(repo_root, data_dir)

    if not os.path.isdir(data_dir):
        print(
            f"ERROR: data directory not found: {data_dir}\n"
            f"Generate the data first:\n"
            f"  pixi run python3 scripts/gen_tpch_parquet.py --sf 1\n",
            file=sys.stderr,
        )
        sys.exit(1)

    RUNS = args.runs
    GPU_READY = (not args.skip_gpu) and _safe_gpu_count() > 0
    POLARS_ON  = _POLARS and not args.skip_polars
    only = None
    if args.queries:
        only = set(int(q.strip()) for q in args.queries.split(","))

    # ── Load data ──────────────────────────────────────────────────────────
    T = load_tables(data_dir)

    # Convenience references
    lineitem  = T.get("lineitem")
    orders    = T.get("orders")
    customer  = T.get("customer")
    part      = T.get("part")
    supplier  = T.get("supplier")
    partsupp  = T.get("partsupp")
    nation    = T.get("nation")
    region    = T.get("region")

    # Polars equivalents (lazy conversion — only when needed)
    _pl_cache: dict[str, object] = {}
    def _pl(name: str):
        if not POLARS_ON:
            return None
        if name not in _pl_cache:
            _pl_cache[name] = _to_polars(T[name])
        return _pl_cache[name]

    li_rows = len(lineitem) if lineitem is not None else 0

    print(f"TPC-H Real-Data Benchmark  ({li_rows:,} lineitem rows, {RUNS} hot runs)")
    print(f"Polars: {'on' if POLARS_ON else 'off'}   GPU: {'ready' if GPU_READY else 'not available'}")
    print()
    print("⚠️  TPC-H® is a trademark of TPC. "
          "This is an independent non-certified benchmark.")

    rows = []  # (label, pd_ms, pl_ms, cpu_ms, gpu_ms)

    def skip(n):
        return only is not None and n not in only

    def bench(label: str, qn: int, pd_fn, pl_fn, cpu_fn, gpu_fn):
        if skip(qn):
            return
        pd_ms  = _time(pd_fn, RUNS)
        pl_ms  = _time(pl_fn, RUNS) if (POLARS_ON and pl_fn) else None
        cpu_ms = _time(cpu_fn, RUNS)
        gpu_ms = None
        if GPU_READY and gpu_fn:
            try:
                gpu_ms = _time(gpu_fn, RUNS)
            except Exception:
                gpu_ms = None
        rows.append((label, pd_ms, pl_ms, cpu_ms, gpu_ms))
        _print_row(label, pd_ms, pl_ms, cpu_ms, gpu_ms)

    _print_header()

    # ── Q1 ─────────────────────────────────────────────────────────────────
    bench("Q1  filter+groupby+8agg", 1,
        lambda: bm.run_q1_pandas(lineitem),
        lambda: bm.run_q1_polars(lineitem),
        lambda: bm.run_q1_mxframe(lineitem, device="cpu"),
        lambda: bm.run_q1_mxframe(lineitem, device="gpu"))

    # ── Q2 ─────────────────────────────────────────────────────────────────
    bench("Q2  min-cost supplier", 2,
        lambda: bm.run_q2_pandas(nation, region, part, supplier, partsupp),
        lambda: bm.run_q2_polars(nation, region, part, supplier, partsupp) if hasattr(bm, "run_q2_polars") else None,
        lambda: bm.run_q2_mxframe(nation, region, part, supplier, partsupp, device="cpu"),
        lambda: bm.run_q2_mxframe(nation, region, part, supplier, partsupp, device="gpu"))

    # ── Q3 ─────────────────────────────────────────────────────────────────
    bench("Q3  3-join+groupby", 3,
        lambda: bm.run_q3_pandas(customer, orders, lineitem),
        lambda: bm.run_q3_polars(customer, orders, lineitem),
        lambda: bm.run_q3_mxframe(customer, orders, lineitem, device="cpu"),
        lambda: bm.run_q3_mxframe(customer, orders, lineitem, device="gpu"))

    # ── Q4 ─────────────────────────────────────────────────────────────────
    bench("Q4  EXISTS semi-join", 4,
        lambda: bm.run_q4_pandas(orders, lineitem),
        lambda: bm.run_q4_polars(orders, lineitem),
        lambda: bm.run_q4_mxframe(orders, lineitem, device="cpu"),
        lambda: bm.run_q4_mxframe(orders, lineitem, device="gpu"))

    # ── Q5 ─────────────────────────────────────────────────────────────────
    bench("Q5  5-join+groupby", 5,
        lambda: bm.run_q5_pandas(nation, customer, orders, lineitem),
        lambda: bm.run_q5_polars(nation, customer, orders, lineitem),
        lambda: bm.run_q5_mxframe(nation, customer, orders, lineitem, device="cpu"),
        lambda: bm.run_q5_mxframe(nation, customer, orders, lineitem, device="gpu"))

    # ── Q6 ─────────────────────────────────────────────────────────────────
    bench("Q6  filter+global sum", 6,
        lambda: bm.run_q6_pandas(lineitem),
        lambda: bm.run_q6_polars(lineitem),
        lambda: bm.run_q6_mxframe(lineitem, device="cpu"),
        lambda: bm.run_q6_mxframe(lineitem, device="gpu"))

    # ── Q7 ─────────────────────────────────────────────────────────────────
    bench("Q7  nation join+groupby", 7,
        lambda: bm.run_q7_pandas(nation, supplier, customer, orders, lineitem),
        lambda: bm.run_q7_polars(nation, supplier, customer, orders, lineitem),
        lambda: bm.run_q7_mxframe(nation, supplier, customer, orders, lineitem, device="cpu"),
        lambda: bm.run_q7_mxframe(nation, supplier, customer, orders, lineitem, device="gpu"))

    # ── Q8 ─────────────────────────────────────────────────────────────────
    bench("Q8  market share", 8,
        lambda: bm.run_q8_pandas(nation, region, part, customer, orders, lineitem),
        lambda: bm.run_q8_polars(nation, region, part, customer, orders, lineitem),
        lambda: bm.run_q8_mxframe(nation, region, part, customer, orders, lineitem, device="cpu"),
        lambda: bm.run_q8_mxframe(nation, region, part, customer, orders, lineitem, device="gpu"))

    # ── Q9 ─────────────────────────────────────────────────────────────────
    bench("Q9  6-join+profit", 9,
        lambda: bm.run_q9_pandas(nation, supplier, partsupp, part, orders, lineitem),
        lambda: bm.run_q9_polars(nation, supplier, partsupp, part, orders, lineitem),
        lambda: bm.run_q9_mxframe(nation, supplier, partsupp, part, orders, lineitem, device="cpu"),
        lambda: bm.run_q9_mxframe(nation, supplier, partsupp, part, orders, lineitem, device="gpu"))

    # ── Q10 ────────────────────────────────────────────────────────────────
    bench("Q10 returned items", 10,
        lambda: bm.run_q10_pandas(nation, customer, orders, lineitem),
        lambda: bm.run_q10_polars(nation, customer, orders, lineitem),
        lambda: bm.run_q10_mxframe(nation, customer, orders, lineitem, device="cpu"),
        lambda: bm.run_q10_mxframe(nation, customer, orders, lineitem, device="gpu"))

    # ── Q11 ────────────────────────────────────────────────────────────────
    bench("Q11 inventory value", 11,
        lambda: bm.run_q11_pandas(nation, supplier, partsupp),
        lambda: bm.run_q11_polars(nation, supplier, partsupp) if hasattr(bm, "run_q11_polars") else None,
        lambda: bm.run_q11_mxframe(nation, supplier, partsupp, device="cpu"),
        lambda: bm.run_q11_mxframe(nation, supplier, partsupp, device="gpu"))

    # ── Q12 ────────────────────────────────────────────────────────────────
    bench("Q12 shipping modes", 12,
        lambda: bm.run_q12_pandas(orders, lineitem),
        lambda: bm.run_q12_polars(orders, lineitem),
        lambda: bm.run_q12_mxframe(orders, lineitem, device="cpu"),
        lambda: bm.run_q12_mxframe(orders, lineitem, device="gpu"))

    # ── Q13 ────────────────────────────────────────────────────────────────
    bench("Q13 customer orders", 13,
        lambda: bm.run_q13_pandas(customer, orders),
        lambda: bm.run_q13_polars(customer, orders) if hasattr(bm, "run_q13_polars") else None,
        lambda: bm.run_q13_mxframe(customer, orders, device="cpu"),
        lambda: bm.run_q13_mxframe(customer, orders, device="gpu"))

    # ── Q14 ────────────────────────────────────────────────────────────────
    bench("Q14 promo revenue", 14,
        lambda: bm.run_q14_pandas(part, lineitem),
        lambda: bm.run_q14_polars(part, lineitem),
        lambda: bm.run_q14_mxframe(part, lineitem, device="cpu"),
        lambda: bm.run_q14_mxframe(part, lineitem, device="gpu"))

    # ── Q15 ────────────────────────────────────────────────────────────────
    bench("Q15 top supplier", 15,
        lambda: bm.run_q15_pandas(supplier, lineitem),
        lambda: bm.run_q15_polars(supplier, lineitem) if hasattr(bm, "run_q15_polars") else None,
        lambda: bm.run_q15_mxframe(supplier, lineitem, device="cpu"),
        lambda: bm.run_q15_mxframe(supplier, lineitem, device="gpu"))

    # ── Q16 ────────────────────────────────────────────────────────────────
    bench("Q16 part/supplier", 16,
        lambda: bm.run_q16_pandas(part, supplier, partsupp),
        lambda: bm.run_q16_polars(part, supplier, partsupp) if hasattr(bm, "run_q16_polars") else None,
        lambda: bm.run_q16_mxframe(part, supplier, partsupp, device="cpu"),
        lambda: bm.run_q16_mxframe(part, supplier, partsupp, device="gpu"))

    # ── Q17 ────────────────────────────────────────────────────────────────
    bench("Q17 small qty revenue", 17,
        lambda: bm.run_q17_pandas(part, lineitem),
        lambda: bm.run_q17_polars(part, lineitem) if hasattr(bm, "run_q17_polars") else None,
        lambda: bm.run_q17_mxframe(part, lineitem, device="cpu"),
        lambda: bm.run_q17_mxframe(part, lineitem, device="gpu"))

    # ── Q18 ────────────────────────────────────────────────────────────────
    bench("Q18 large volume", 18,
        lambda: bm.run_q18_pandas(customer, orders, lineitem),
        lambda: bm.run_q18_polars(customer, orders, lineitem) if hasattr(bm, "run_q18_polars") else None,
        lambda: bm.run_q18_mxframe(customer, orders, lineitem, device="cpu"),
        lambda: bm.run_q18_mxframe(customer, orders, lineitem, device="gpu"))

    # ── Q19 ────────────────────────────────────────────────────────────────
    bench("Q19 discounted revenue", 19,
        lambda: bm.run_q19_pandas(part, lineitem),
        lambda: bm.run_q19_polars(part, lineitem) if hasattr(bm, "run_q19_polars") else None,
        lambda: bm.run_q19_mxframe(part, lineitem, device="cpu"),
        lambda: bm.run_q19_mxframe(part, lineitem, device="gpu"))

    # ── Q20 ────────────────────────────────────────────────────────────────
    bench("Q20 potential parts", 20,
        lambda: bm.run_q20_pandas(nation, part, supplier, partsupp, lineitem),
        lambda: bm.run_q20_polars(nation, part, supplier, partsupp, lineitem) if hasattr(bm, "run_q20_polars") else None,
        lambda: bm.run_q20_mxframe(nation, part, supplier, partsupp, lineitem, device="cpu"),
        lambda: bm.run_q20_mxframe(nation, part, supplier, partsupp, lineitem, device="gpu"))

    # ── Q21 ────────────────────────────────────────────────────────────────
    bench("Q21 failed parts", 21,
        lambda: bm.run_q21_pandas(nation, supplier, orders, lineitem),
        lambda: bm.run_q21_polars(nation, supplier, orders, lineitem) if hasattr(bm, "run_q21_polars") else None,
        lambda: bm.run_q21_mxframe(nation, supplier, orders, lineitem, device="cpu"),
        lambda: bm.run_q21_mxframe(nation, supplier, orders, lineitem, device="gpu"))

    # ── Q22 ────────────────────────────────────────────────────────────────
    bench("Q22 global sales opp", 22,
        lambda: bm.run_q22_pandas(customer, orders),
        lambda: bm.run_q22_polars(customer, orders) if hasattr(bm, "run_q22_polars") else None,
        lambda: bm.run_q22_mxframe(customer, orders, device="cpu"),
        lambda: bm.run_q22_mxframe(customer, orders, device="gpu"))

    # ── Summary ────────────────────────────────────────────────────────────
    print()
    print(f"All times in ms (min over {RUNS} hot runs after 1 warmup).")
    print(f"Data: TPC-H SF={_infer_sf(data_dir)} via DuckDB dbgen | {li_rows:,} lineitem rows")
    print()

    if rows:
        cpu_beats = sum(1 for _, pd, pl, cpu, _ in rows if pl and cpu and cpu < pl)
        gpu_beats = sum(1 for _, pd, pl, cpu, gpu in rows if pl and gpu and gpu < pl)
        n = len(rows)
        print(f"MX CPU faster than Polars: {cpu_beats}/{n} queries")
        if GPU_READY:
            print(f"MX GPU faster than Polars: {gpu_beats}/{n} queries")


def _infer_sf(data_dir: str) -> str:
    """Best-effort extract SF from directory name like 'tpch_sf1'."""
    base = os.path.basename(data_dir.rstrip("/\\"))
    if "sf" in base:
        try:
            return base.split("sf")[-1]
        except Exception:
            pass
    return "?"


if __name__ == "__main__":
    main()
